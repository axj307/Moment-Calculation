import numpy as np


def convert2indexNotation(E11, E22, E33, E44):
    
    # for 2 D system
    M1 = E11.T
    if len(E11) == 2:
        M2 = np.array([ E22[0,0] , E22[0,1], E22[1,1] ])        
        M3 = np.array([ E33[0, 0, 0] , E33[0, 0, 1], E33[0, 1, 1], E33[1, 1, 1] ])
        M4 = np.array([ E44[0, 0, 0, 0] , E44[0, 0, 0, 1], E44[0, 0, 1, 1], E44[1, 1, 1, 0], E44[1, 1, 1, 1] ])
    
    if len(E11) ==3:
        M2 = np.array([ E22[0,0] , E22[0,1], E22[1,1], E22[2, 0], E22[2, 1], E22[2, 2]])        
        M3 = np.array([ E33[0, 0, 0] , E33[0, 0, 1], E33[0, 1, 1], E33[1, 1, 1], E33[0, 0, 2], E33[0, 1, 2], E33[1, 1, 2], E33[0, 2, 2], E33[1, 2, 2], E33[2, 2, 2] ])
        
        M4 = np.array([ E44[0, 0, 0, 0] , E44[0, 0, 0, 1], E44[0, 0, 1, 1], E44[1, 1, 1, 0], E44[1, 1, 1, 1] , E44[0, 0, 0, 2], E44[0, 0, 1, 2], E44[0, 1, 1, 2] \
                       , E44[1, 1, 1, 2], E44[0, 0, 2, 2], E44[0, 1, 2, 2], E44[1, 1, 2, 2], E44[0, 2, 2, 2], E44[1, 2, 2, 2], E44[2, 2, 2, 2]])
            
    
    
    return(M1, M2, M3, M4)
    
    
    
    
    
    
